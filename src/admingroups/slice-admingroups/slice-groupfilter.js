import { createSlice, createAsyncThunk } from '@reduxjs/toolkit'
import axios from 'axios'
import * as APIConstants from "../../hsStore/ApiConstants"

// API call
export const groupsFilter_APIcall = createAsyncThunk('groupsFilterData/groupsFilter_APIcall', async (payload) => {
    const apiURL = APIConstants.APIbaseURL + APIConstants.groupsFilterPath
    const response = await axios.post(apiURL, payload)
    
    console.log("groups filtered Data", response)
    return response.data
})

//reducer
export const GroupsFilter_slice = createSlice({ 
    name: 'groupsFilterData',
    initialState: {
        data: [],
        loading: 'idle',
        error: null
    },
    reducers: {},
    extraReducers: (builder) => {
        builder.addCase(groupsFilter_APIcall.pending, (state, action) => {
            console.log("getting1", state.loading)
            if (state.loading === 'idle') {
                state.loading = 'pending'
            }
        })
        builder.addCase(groupsFilter_APIcall.fulfilled, (state, action) => {
            console.log("getting2", state.loading)
            if (state.loading === 'pending') {
                state.data = action.payload
                state.loading = 'idle'
            }
        })
        builder.addCase(groupsFilter_APIcall.rejected, (state, action) => {
            console.log("getting3", state.loading)
            if (state.loading === 'pending') {
                state.loading = 'idle'
                state.error = 'Error occured'
            }
        })
    }
})


export default GroupsFilter_slice.reducer
