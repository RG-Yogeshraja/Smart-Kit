import { createSlice, createAsyncThunk } from '@reduxjs/toolkit'
import axios from 'axios'
import * as APIConstants from "../../hsStore/ApiConstants"

// API call
export const updateEvent_APIcall = createAsyncThunk('updateEventData/updateEvent_APIcall', async (payloadd) => {
    const apiURL = APIConstants.APIbaseURL + APIConstants.updateEventPath
    const response = await axios.post(apiURL, payloadd)
    
    console.log("Update event data", response)
    return response.data
})

//reducer
export const updateEvent_slice = createSlice({
    name: 'updateEventData',
    initialState: {
        data: [],
        loading: 'idle',
        error: null
    },
    reducers: {},
    extraReducers: (builder) => {
        builder.addCase(updateEvent_APIcall.pending, (state, action) => {
            console.log("getting1", state.loading)
            if (state.loading === 'idle') {
                state.loading = 'pending'
            }
        })
        builder.addCase(updateEvent_APIcall.fulfilled, (state, action) => {
            console.log("getting2", state.loading)
            if (state.loading === 'pending') {
                state.data = action.payload
                state.loading = 'idle'
            }
        })
        builder.addCase(updateEvent_APIcall.rejected, (state, action) => {
            console.log("getting3", state.loading)
            if (state.loading === 'pending') {
                state.loading = 'idle'
                state.error = 'Error occured'
            }
        })
    }
})


export default updateEvent_slice.reducer

